/**
 * Settings for the game.
 */
export class Settings {
    /**
     * The number of pairs of piles.
     */
    public static readonly PairsOfPiles = 2

    /**
     * The size of valid backward jumps on a pile.
     */
    public static readonly JumpBackSize = 10

    /**
     * The start value for descending piles.
     */
    public static readonly TopLimit = 100

    /**
     * The size of a player's hand.
     */
    public static readonly HandSize = 7
}
